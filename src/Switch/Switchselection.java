package Switch;

public class Switchselection {

	public static void main(String[] args) {
		int a = 0;
		switch(a) {
		
		case 0:
			System.out.println("inside case 0");
		System.out.println("inside case 0");
		System.out.println("inside case 0");
		break;
	case 1:
		System.out.println("inside case 1");
	System.out.println("inside case 1");
	System.out.println("inside case 1");
	break;
	case 2:
		System.out.println("inside case 2");
		System.out.println("inside case 2");
		System.out.println("inside case 2");
		default:
			System.out.println("inside default, no case is matching");
		

		}

	}

}
